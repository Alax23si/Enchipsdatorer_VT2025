/* USER CODE BEGIN 0 */
int8_t step_index = 0;


const uint8_t step_seq[8][4] = {
		{1, 0, 0, 0}, // Steg 1
		{1, 1, 0, 0}, // Steg 2
		{0, 1, 0, 0}, // Steg 3
		{0, 1, 1, 0}, // Steg 4
		{0, 0, 1, 0}, // Steg 5
		{0, 0, 1, 1}, // Steg 6
		{0, 0, 0, 1}, // Steg 7
		{1, 0, 0, 1}  // Steg 8
};

void StepperMove(int8_t direction) {
	if (direction > 0) {
		step_index++;
		if (step_index > 7) step_index = 0; // Börja om
	} else if (direction < 0) {
		step_index--;
		if (step_index < 0) step_index = 7; // Gå baklänges
	}

	// Sätt pinnarna
	HAL_GPIO_WritePin(IN1_GPIO_Port, IN1_Pin, step_seq[step_index][0]);
	HAL_GPIO_WritePin(IN2_GPIO_Port, IN2_Pin, step_seq[step_index][1]);
	HAL_GPIO_WritePin(IN3_GPIO_Port, IN3_Pin, step_seq[step_index][2]);
	HAL_GPIO_WritePin(IN4_GPIO_Port, IN4_Pin, step_seq[step_index][3]);
}
